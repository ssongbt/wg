package com.tjoeun.vo;

public class WgLocationVO {
	private int loc_num;
	private String location;
	private String latitude;
	private String longitude;
	private String gu_location;
	
	
	public int getLoc_num() {
		return loc_num;
	}
	public void setLoc_num(int loc_num) {
		this.loc_num = loc_num;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getGu_location() {
		return gu_location;
	}
	public void setGu_location(String gu_location) {
		this.gu_location = gu_location;
	}
	
	
	
}
